func CriarUsuario(w http.ResponseWriter, r *http.Request) {
	corpoRequest, erro := ioutil.ReadAll(r.Body) // vai ler o corpo da requisicao
	if erro != nil {                             // trata se tiver algim erro
		respostas.Erro(w, http.StatusUnprocessableEntity, erro) // encerra o programa se tiver algum erro
		return
	}
	var usuario modelos.Usuario
	//converte o corpo da requisicao que esta em json para um objeto usuario (model)
	if erro = json.Unmarshal(corpoRequest, &usuario); erro != nil {
		respostas.Erro(w, http.StatusBadRequest, erro)
		return
	}

	if erro = usuario.Preparar("cadastro"); erro != nil {
		respostas.Erro(w, http.StatusBadRequest, erro)
		return
	}
	//conecta com o banco
	db, erro := banco.Conectar()
	if erro != nil {
		respostas.Erro(w, http.StatusInternalServerError, erro)
		return
	}

	//cria um novo repositorio de usuarios com a conexao do banco
	repositorio := repositorios.NovoRepositorioDeUsuarios(db)

	// chama a funcao criar para inserir nvo usuário no banco de dados
	usuario.ID, erro = repositorio.Criar(usuario)
	if erro != nil {
		respostas.Erro(w, http.StatusInternalServerError, erro)
		return
	}

	//resposta http
	respostas.JSON(w, http.StatusCreated, usuario)

}

func BuscarUsuarios(w http.ResponseWriter, r *http.Request) {
	nomeouNick := strings.ToLower(r.URL.Query().Get("usuario")) //vai pegar o usuario atraves do query params
	db, erro := banco.Conectar()
	if erro != nil {
		respostas.Erro(w, http.StatusInternalServerError, erro)
		return
	}
	defer db.Close()

	repositorio := repositorios.NovoRepositorioDeUsuarios(db)

	usuarios, erro := repositorio.Buscar(nomeouNick)
	if erro != nil {
		respostas.Erro(w, http.StatusInternalServerError, erro)
		return
	}
	respostas.JSON(w, http.StatusOK, usuarios)
}

